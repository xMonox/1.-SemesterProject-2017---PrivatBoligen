CREATE DATABASE  IF NOT EXISTS `privatboligen`;
USE `privatboligen`;

create table customers
(
  customer_customercvr int not null
    primary key,
  customer_customername varchar(45) not null,
  customer_address varchar(45) not null,
  customer_zipcode int not null,
  customer_contactperson varchar(45) not null,
  customer_telephonenumber int not null,
  customer_email varchar(30) not null
)
;

create index zipcode_zip_idx
  on customers (customer_zipcode)
;
create table files
(
  file_fileid int auto_increment
    primary key,
  file_filename varchar(45) not null,
  file_file longblob not null
)
;

create table invoices
(
  invoice_invoicenumber int not null
    primary key,
  invoice_customercvr int not null,
  invoice_invoicedate date not null,
  invoice_paymentdate date not null,
  invoice_fileid int not null,
  invoice_userid int not null,
  constraint customer_customerid
  foreign key (invoice_customercvr) references privatboligen.customers (customer_customercvr)
    on delete cascade,
  constraint file_file
  foreign key (invoice_fileid) references privatboligen.files (file_fileid)
    on delete cascade
)
;

create index customer_customerid_idx
  on invoices (invoice_customercvr)
;

create index file_file_idx
  on invoices (invoice_fileid)
;

create index user_userid_idx
  on invoices (invoice_userid)
;

create table users
(
  user_userid int auto_increment
    primary key,
  user_username varchar(25) not null,
  user_password varchar(150) not null,
  user_role varchar(15) not null
)
;

alter table invoices
  add constraint user_userid
foreign key (invoice_userid) references privatboligen.users (user_userid)
;

create table zipcodes
(
  zipcode_zip int not null
    primary key,
  zipcode_city varchar(20) not null
)
;

alter table customers
  add constraint zipcode_zip
foreign key (customer_zipcode) references privatboligen.zipcodes (zipcode_zip)
;

